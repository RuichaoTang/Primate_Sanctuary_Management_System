package sanctuary;

/**
 * This enum represents various types of foods that animals may consume.
 */
public enum Foods {
    /**
     * Eggs.
     */
    EGGS,

    /**
     * Fruits.
     */
    FRUITS,

    /**
     * Insects.
     */
    INSECT,

    /**
     * Leaves.
     */
    LEAVES,

    /**
     * Nuts.
     */
    NUTS,

    /**
     * Seeds.
     */
    SEEDS,

    /**
     * Tree sap.
     */
    TREESAP
}

